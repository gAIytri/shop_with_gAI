function img(name: string, n: number = 4): Product['images'] {
  const encoded = encodeURIComponent(name)
  return Array.from({ length: n }, (_, i) => ({
    id: `img-${encoded}-${i}`,
    url: `https://placehold.co/600x600/F9F9F9/1A1A1A?text=${encoded}+${i + 1}`,
    alt: `${name} - view ${i + 1}`,
    isPrimary: i === 0,
    displayOrder: i,
  }))
}

